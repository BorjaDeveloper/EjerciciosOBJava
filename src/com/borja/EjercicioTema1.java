package com.borja;

public class EjercicioTema1 {

    public static void main(String[] args) {

        byte dato1 = 2;
        short dato2 = 16;
        int dato3 = 41;
        long dato4 = 125;

        System.out.println(dato1);
        System.out.println(dato2);
        System.out.println(dato3);
        System.out.println(dato4);


        float dato5 = 1.2f;
        double dato6 = 125.25d;

        System.out.println(dato5);
        System.out.println(dato6);

        boolean dato7 = true;
        boolean dato8 = false;

        char dato9 = 'a';
        String dato10 = "Mi nombre es: ";
        String dato11 = "Borja";

        System.out.println(dato7);
        System.out.println(dato8);
        System.out.println(dato9);
        System.out.println(dato10);
        System.out.println(dato11);
    }
}
